                        <h3>Poinçonneuse</h3>
                    </div>
                    <div class="description">
                        <p>Une poinçonneuse est un outil ou une machine-outil destiné à réaliser un poinçonnage dans diverses matières par l'intermédiaire d'un poinçon. La poinçonneuse permet le marquage, le découpage ou la perforation d'un objet.
                        </p>
