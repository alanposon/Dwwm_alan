                        <h2>Norme soudure </h2>

                    </div>

                    <div class="descriptionA">
                        <p>La soudure, la norme et les normes de prévention des accidents.
                            <br> La soudure à l'arc, au fer ou au chalumeau oxygène acétylène requiert des techniques
                            pour savoir comment souder.
                            <br> Les normes employées sont rédigées :
                            <br>> au plan national : par l'AFNOR,
                            <br>> au plan européen : par le CERN,
                            <br>> au plan international : par l'ISO.
                        </p>
                        <h3>Soudure : les types de normes</h3>
                        <p>Il existe différents types de normes pour la soudure :
                            <br>> normes de conception des postes à souder,
                            <br>> normes de sécurité et de protection,
                            <br>> normes de soudage.
                        </p>
                        <h3>Les principales normes de conception</h3>
                        <h4>La protection contre les chocs électriques</h4>
                        <p>IP 23 est l'indice de protection minimal d'un poste à souder à l'arc.
                            <br>> IP = Indice de Protection
                            <br>> 2 = Un objet de plus de 12,5 mm de diamètre ne peut pas pénétrer dans l'appareil et
                            venir en contact avec un élément interne sous tension dangereuse.
                            <br>> 3 = La source de courant est protégée contre toute détérioration en cours
                            d'utilisation par de l'eau tombant en pluie avec un angle maximal de 60°.
                        </p>
                        <h4>La prévention des harmoniques</h4>
                        <p>Afin de prévenir la création d'harmoniques dans les réseaux électriques, la norme EN
                            61000-3-12 est applicable à l'utilisation
                            <br>> Les postes à souder utilisés dans des locaux industriels raccordés à la haute tension
                            via un transformateur local ou à la basse tension sans transformateur local doivent être de
                            classe A.
                            <br>> Les postes à souder utilisés dans des maisons alimentées en basse tension sans
                            transformateur local ont une limite de construction sur la génération d'harmoniques et
                            doivent être de classe B.

                        </p>
                        <h3>Principales normes de sécurité et de protection</h3>
                        <h4> Normes de sécurité et de protection </h4>
                        <p><strong>NF EN ISO 15011-4 : </strong>Méthode de laboratoire d'échantillonnage des fumées et
                            des gaz émis par le soudage</p>
                        <br> <strong>NF EN 15012-1 : </strong>Exigences, essais et marquage des équipements de
                        filtration d'air
                        <h3>La principale norme de soudage</h3>
                        <p>La norme qui regroupe les procédés et dispositifs est la norme <strong>EN 287-2004 :</strong>
                            <br>
                            <br>> Les procédés de soudage :
                            <br>- Soudage manuel à l'arc avec électrode enrobée.
                            <br>- Soudage à l'arc avec fil fourré auto-protecteur.
                            <br>- Soudage à l'arc sous flux avec un seul fil.
                            <br>- Soudage à l'arc sous flux avec fil fourré.
                            <br>- Soudage MIG.
                            <br>- Soudage MAG avec fil fusible.
                            <br>- Soudage MAG avec fil fourré.
                            <br>- Soudage TIG.
                            <br>- Soudage Plasma.
                            <br>- Soudage oxyacétylénique.
                            <br>- Soudage MIG avec fil fourré.
                            <br>> Les groupes d'aciers.
                            <br>> Le domaine de validité des groupes de matériaux.
                            <br>> Les produits consommables.
                            <br>> Les positions de soudage.
                            <br>> Le domaine de validité pour l'épaisseur de matériau.
                            <br>> Le domaine de validité pour le diamètre.
                            <br>> Les piquages.
                            <br>> Les multi assemblages.
                            <br>> Les multi procédés.
                            <br>> Désignation.
                            <br>> Reconduction.
                            <br>> Prolongation.
                        </p>
