            <h3>Tuyauteur</h3>
            <p>Tuyauteur est un métier très technique qui consiste à intervenir
                sur des réseaux de tuyauteries qui conduisent des fluides liquides ou gazeux
                , explosifs ou corrosifs, à des pressions et températures plus ou moins élevées.
                Il doit étudier les plans de construction du réseau, les matériaux utilisés,
                les soudures et déterminer les travaux à réaliser : démontage, découpe et
                éventuellement le remplacement de portions de tuyauterie en mauvais état.
                Il prépare les éléments de tuyauteries par débit, oxycoupage ou cintrage et
                les assemblages par boulonnage, pointage ou vissage. Il contrôle les éléments réalisés et le
                dossier de maintenance. Les quatre principales étapes de son travail sont le découpage, le
                piquage (intersection d'un tube par un autre), le formage (filetage, cintrage) et enfin
                l'assemblage. Il porte une tenue spéciale et
                des équipements de protection type casque anti-bruit et masque à air.</p>
