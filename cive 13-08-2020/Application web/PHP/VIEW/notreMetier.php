            <h3>Monteur</h3>
            <p>Le monteur assemble un système mécanique (et parfois électrique) à partir de pièces qui lui ont été fournies.
                Aussi appelé ajusteur-monteur ou opérateur de montage, le monteur débute sa mission par la réception des éléments à assembler. Il les identifie et les vérifie. Il étudie le plan qui lui a été communiqué et qui dans bien cas est présenté en anglais.
                Le monteur corrige les éventuels défauts des pièces, ou les façonne afin qu'elles correspondent mieux à leur usage final. Il les lime, les perce, les ponce ou les plie selon un cahier des charges propre à l'entreprise.
                Il assemble des mécanismes plus ou moins complexes, ajuste des pièces pour qu'elles coïncident parfaitement. Il soude, rivette, ou visse les éléments entre eux.
                Une fois le montage fini, le monteur vérifie le bon fonctionnement de l'ensemble, mais aussi la qualité de fonctionnement. Il règle la tension des courroies, graisse les systèmes à chaîne ou bien fait le niveau des vérins hydrauliques. Il met tout en œuvre pour assurer la durabilité de la machine qu'il vient d'assembler.
                En tant que spécialiste, le monteur peut être amené à exprimer des recommandations à l'agent de maintenance, ou à exécuter lui-même l'entretien ultérieur.</p>
