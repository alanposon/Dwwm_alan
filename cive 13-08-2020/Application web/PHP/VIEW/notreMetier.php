            <h3>Soudeur</h3>
            <p>Le soudeur assemble les pièces préparées par les chaudronniers et les tuyauteurs :
                tuyaux, brides, tôle formé, grands ensembles chaudronnés pour l'industrie..
                Avant de procéder à la soudure, il prépare les pièces à souder, les dégraisse et les
                décape.
                Il porte une combinaison et un masque pour se protéger contre d’éventuelles projections.
                Il allume la flamme (soudure au chalumeau) ou branche le contact électrique (soudure à
                l’arc électrique ou laser.. ). Il règle l’intensité de la flamme ou du courant et
                programme la vitesse de soudage.
                Il maintient l’appareil de soudage à une distance précise de la pièce à souder et évalue
                la durée d’exposition nécessaire à la réussite de l’opération. Il nettoie ensuite la
                soudure, la contrôle et la polit. Il est responsable de l’entretien de son matériel de
                travail.
                Dans certains cas, le soudeur peut être spécialisé par type d’installation, type
                d’assemblage, secteur d’activité, procédé, métal, position de soudage et type de pièces.
                En effet, les procédés sont multiples et parfois très techniques : soudage à l'arc
                électrique pour la construction mécanique, soudage laser pour les soudages de haute
                précision (industrie, médical).
                Le soudeur travaille dans un atelier ou sur un chantier. Il travaille le plus souvent
                seul. Il est constamment debout. Pour certaines opérations de soudure, il peut
                travailler dans des positions inconfortables et devoir porter un masque pour filtrer des
                poussières.
                Il peut travailler en équipe avec des horaires différents (matin, après midi, nuit) dans
                un atelier qui fonctionne 24 h sur 24.
                Les conditions de travail précises varient selon les types d’entreprises. Elles ne sont
                pas les mêmes s’il intervient sur une plate-forme pétrolière ou une centrale nucléaire
                ou encore dans une entreprise de tuyauterie industrielle en travaux publics.</p>

