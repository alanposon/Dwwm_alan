            <img src="IMAGE/plieuse.jpg" alt="plieuse">
            <h3>Une plieuse</h3>
            <p> C’est dans le secteur de la chaudronnerie et
                de la métallerie que l’on rencontre souvent ce type de machine.
                Son principal rôle est de plier une fine feuille de métal,
                d’où son nom « presse plieuse ». Cette dernière est principalement
                constituée d’une matrice en forme de V, que l’on appelle le vé,
                d’un poinçon qui contre la matrice et appelé contre -vé.
                Il se peut aussi que la matrice soit en U ou dans une forme
                qui va permettre d’obtenir le profil du pliage recherché.
            </p>
        </div>

        <div class="contenu2">
            <img src="IMAGE/cisaille.jpg" alt="cisaille guillotine">
            <h3>Une cisaille guillotine</h3>
            <p>La cisaille guillotine sert à découper les tôles.
                Elle est largement répandue dans l'industrie transformatrice
                des métaux, principalement dans les chaudronneries et
                les métalleries.
                Agissant comme la lame d'une guillotine ou d'une paire
                de ciseaux, la lame supérieure va pénétrer le métal et
                le coincer sur une lame inférieure. Il existe deux principaux types de coupe :
                <br> <br>> une coupe en forme de triangle rectangle, la lame descend inclinée ;
                <br>> une coupe en forme de rectangle, la lame descend droite.
            </p>
        </div>

        <div class="contenu2">
            <img src="IMAGE/perceuse.jpg" alt="Une perceuse à colonne">
            <h3>Une perceuse à colonne</h3>
            <p>> Une perceuse à colonne est une machine-outil
                d'atelier fixée sur un bâti, un établi ou au sol.
                Elle permet des perçages verticaux précis et importants
                (diamètres pouvant aller jusqu'à 30 millimètres dans
                l'acier ordinaire).
            </p>
        </div>

    </div>

