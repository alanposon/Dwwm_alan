            <h3>Une scie à ruban</h3>
            <p>La scie à ruban est une machine-outil qui met en rotation une bande en acier fermée sur elle-même ; elle sert principalement au délignage de plateaux en menuiserie. Elle permet également le chantournage en utilisant des lames de faible largeur. Son action diffère de celle de la scie circulaire notamment par sa hauteur de coupe et ses capacités de chantournage.
            </p>
        </div>

        <div class="contenu2">
